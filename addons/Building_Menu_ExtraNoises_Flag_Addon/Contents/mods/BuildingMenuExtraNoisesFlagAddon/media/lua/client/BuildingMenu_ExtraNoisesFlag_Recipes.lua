local function flagRecipeExists(colors)
    local recipeName = table.concat(colors, "") .. "FlagRecipe"
    return BuildingMenu[recipeName] ~= nil
end

local function generateFlagRecipes()
    if BuildingMenu.Tools.Scissors == nil then
        BuildingMenu.Tools.Scissors = {
            types = {
                'Base.Scissors',
                'ToolsOfTheTrade.BandageScissors',
                'ToolsOfTheTrade.SpiffArmyKnife',
                'ToolsOfTheTrade.Multitool'
            },
            tags = {'Scissors'}
        }
    end

    for _, colors in pairs(flagColors) do
        for i = 0, 1 do
            local type = ""
            if i == 0 then
                type = "Small"
            end

            local recipeName = table.concat(colors, "") .. "Flag".. type .."Recipe"
            if not flagRecipeExists(colors) then

                -- Generate new recipe
                local newRecipe = {
                    neededTools = {
                        "Scissors",
                        "Needle",
                        "Paintbrush"
                    },
                    neededMaterials = {
                        {
                            Material = "Base.RippedSheets",
                            Amount = 2 + i * 4
                        },
                        {
                            Material = "Base.Thread",
                            Amount = 1 + i
                        }
                    },
                    useConsumable = {}
                }

                for _, color in ipairs(colors) do
                    table.insert(newRecipe.useConsumable, {
                        Consumable = "Base.Paint" .. color,
                        Amount = 1
                    })
                end

                BuildingMenu[recipeName] = newRecipe

            end
        end
    end
end

function getFlagRecipeName(flagFileName, isSmall)
    local colors = flagColors[flagFileName]

    local size = ""
    if isSmall == true then
        size = "Small"
    end

    if not colors then
        return nil --"RedFlag" .. size .. "Recipe"
    end
    return table.concat(colors, "") .. "Flag" .. size .. "Recipe"
end


